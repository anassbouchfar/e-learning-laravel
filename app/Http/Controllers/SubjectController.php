    public function show(Subject $module)
    {
        
        $coursesDeja=Auth::user()->courses()->get();
        $courses = $module->courses()->get()->map(function($course) use($coursesDeja){
            if($searchCourse=$coursesDeja->firstWhere("id","=",$course->id))
                $course= $searchCourse;
            return $course;
        });
        return view("user.subject.show",["subject"=>$module,"courses"=>$courses]);
    }

